2. Telah selesai mengikuti tes semester ganjil dan genap pada satu tahun pelajaran, baik program pesantren maupun program Kementerian Agama<br>
3. Ketidakhadiran tidak boleh melebihi 15 kali di semester II ( tidak melebihi 24 kali dalam 1 tahun)<br>
4. Nilai K ( dibawah nilai Stsndar KKM) dari masing-masing pelajaran.
