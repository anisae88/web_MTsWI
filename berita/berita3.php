    <p style="text-align: justify; margin-bottom: 30px;">
             <strong>Transparansi Anggaran: Ke Mana Larinya Dana BOS & APP?</strong>
       Menjawab rasa penasaran orang tua, Bapak Sunaryo, S.Pd. memberikan penjelasan rinci mengenai pembiayaan pendidikan. Beliau menegaskan prinsip Inclusive Fee di MTs WI Kebarongan.
“Mungkin di tempat lain biaya bulanan (APP) terlihat lebih murah, tapi sering ada tarikan biaya ujian atau ekstrakurikuler. Di sini, biaya Rp160.000/bulan sudah mencakup semuanya,” jelas beliau.<br>
Rincian Perhitungan:<br>
1. Kebutuhan biaya per santri dalam satu tahun adalah Rp3.015.000.<br>
2. Dana tersebut dipotong bantuan pemerintah (BOS) sebesar Rp1.100.000.<br>
3. Sisa kewajiban adalah Rp1.915.000, yang jika dibagi 12 bulan jatuh pada angka Rp160.000 (pembulatan).<br>
     Dana ini sudah mencakup kegiatan KBM, praktik komputer, buku paket, kalender, hingga 6 kali ujian bagi kelas 9 (UTS, UAS Kemenag, UAS Pesantren, Ujian Madrasah, TKA Nasional, dan TKAD Daerah).<br>
<strong>Kabar Baik: Hutang Gedung Menurun Drastis</strong><br>
     Berkat gerakan “Infaq Subuh” dan keikhlasan para asatidz yang merelakan sebagian gajinya, Bapak Sunaryo, S.Pd. mengabarkan perkembangan positif pembangunan gedung. Dari total hutang awal sebesar 1,4 Miliar Rupiah, saat ini tersisa sekitar 350 Juta Rupiah. Beliau mengucapkan terima kasih atas partisipasi wali santri yang secara tidak langsung ikut beramal jariyah melalui infaq tersebut.<br>
<strong>Agenda Akhir Semester & Bakti Sosial (Baksos)</strong>
    Menutup pertemuan, pihak madrasah mensosialisasikan jadwal padat menjelang kelulusan di bulan Mei mendatang. Mulai akhir Februari, santri kelas 9 akan menghadapi rangkaian simulasi TKA dan ujian akhir.<br>
    Tak lupa, beliau mengundang wali santri untuk hadir dalam acara Bakti Sosial dan Tabligh Akbar pada 15 Maret mendatang yang akan diisi oleh alumni MWI berprestasi, Dr. H. Muhandis Az Zuhri, Lc., MA (Kepala Unit Bahasa UIN K.H. Abdurrahman Wahid Pekalongan & Dai Internasional).
“Alhamdulillah, dana Baksos sudah terkumpul 70 Juta dari target 73 Juta. Ini semua berkat gotong royong kita bersama,” pungkas Bapak Sunaryo, S.Pd.
