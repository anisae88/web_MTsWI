    <p style="text-align: justify; margin-bottom: 30px;">
              <strong>Mendidik Anak: Jihad yang Membutuhkan Kesabaran</strong><br>
    Membuka sambutannya, Kepala MTs WI Kebarongan, Bapak Sunaryo, S.Pd., mengajak wali santri meneladani kesabaran Rasulullah SAW dalam berdakwah selama 13 tahun di Makkah. Beliau menekankan bahwa mengelola anak adalah amanah yang berat namun mulia.
“Kita harus sabar dan tidak pernah putus asa. Bilamana saat ini anak kita masih ada kekurangan, kita berdoa semoga Allah menyempurnakannya di masa depan,” tutur beliau.<br>
<strong>Suka Duka Mendidik: Antara “Ngarit” dan Masjid</strong>
     Dalam momen yang sangat menyentuh dan akrab, Bapak Sunaryo, S.Pd. berbagi pengalaman pribadinya sebagai orang tua. Beliau mengakui bahwa mendampingi anak di rumah adalah tantangan nyata yang juga beliau rasakan.
“Saya pun merasakannya. Pulang dari madrasah jam 3 sore, kepikiran kambing belum dicari rumputnya (ngarit). Setelah lelah ngarit, adzan Maghrib berkumandang. Kadang ada pergolakan batin antara rasa lelah dan keinginan mengajak anak ke masjid. Mendidik anak memang berat, tapi balasan yang besar hanya datang dari kesulitan yang besar pula,” ungkapnya disambut senyum maklum dari para wali santri.<br>
<strong>Filosofi “Menanam Cabai” dan Ancaman “Setan Gepeng”</strong><br>
     Bapak Sunaryo, S.Pd. kembali menguatkan analogi mendidik anak dengan menanam pohon cabai. Beliau menekankan bahwa sekolah hanyalah “tanah subur”, namun perawatan harian tetap harus dilakukan secara sinergis antara guru dan orang tua.
    Beliau menyoroti “rumput liar” zaman sekarang yang paling berbahaya: “Setan Gepeng” atau gadget. “Di madrasah, anak-anak kita jaga lewat pembiasaan shalat dhuha, tadarus, dan absen shalat jamaah. Namun jika di rumah pengaruh gadget dan lingkungan tidak dijaga, maka ‘nutrisi’ kebaikan yang diserap anak di sekolah akan kalah oleh ‘rumput liar’ tersebut,” tegasnya.<br>
    <strong>Mengenal Lebih Dekat: Tim Pengelola & Wali Kelas 9</strong><br>
     Guna memudahkan komunikasi, Bapak Sunaryo, S.Pd. memperkenalkan jajaran struktur organisasi madrasah yang akan mendampingi santri hingga kelulusan nanti:<br>
Manajemen Utama: Bapak Sunaryo, S.Pd. (Kepala), Pak Imam Syuhada (Waka Kurikulum), Bu Kery (Waka Kesiswaan), dan Ustaz Bashiran (Waka Sarpras).
